package cn.lucifer.demo.http.domain;

public class ArrPic {
	private String nID, sImg, sDesc, sTheD;

	/**
	 * @return the nID
	 * @see #nID
	 */
	public String getnID() {
		return nID;
	}

	/**
	 * @param nID
	 *            the nID to set
	 * @see #nID
	 */
	public void setnID(String nID) {
		this.nID = nID;
	}

	/**
	 * @return the sImg
	 * @see #sImg
	 */
	public String getsImg() {
		return sImg;
	}

	/**
	 * @param sImg
	 *            the sImg to set
	 * @see #sImg
	 */
	public void setsImg(String sImg) {
		this.sImg = sImg;
	}

	/**
	 * @return the sDesc
	 * @see #sDesc
	 */
	public String getsDesc() {
		return sDesc;
	}

	/**
	 * @param sDesc
	 *            the sDesc to set
	 * @see #sDesc
	 */
	public void setsDesc(String sDesc) {
		this.sDesc = sDesc;
	}

	/**
	 * @return the sTheD
	 * @see #sTheD
	 */
	public String getsTheD() {
		return sTheD;
	}

	/**
	 * @param sTheD
	 *            the sTheD to set
	 * @see #sTheD
	 */
	public void setsTheD(String sTheD) {
		this.sTheD = sTheD;
	}
}
